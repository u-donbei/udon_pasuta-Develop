    /**
     * ワールドを生成する。
     * このメソッドは、<b>処理に少し時間がかかる可能性がある</b>ので注意してください。
     *
     * @param world 対象のワールド
     */
    public static void buildWorld(World world, GameViewPane target) {
        //生成
        Field[][] worldBlocks = world.getBlocks();

        for (int x = 0; x < worldBlocks.length; x++) {
            Field[] fs = worldBlocks[x];
            for (int y = 0; y < fs.length; y++) {
                AtomicReference<Field> field = new AtomicReference<>();
                switch (DEFAULT_WORLD[y][x]) {
                    case Ground.ID -> {
                        field.set(new Ground());
                        target.addBlock(field.get(), x, y);
                    }
                    case Udon_House.ID -> {
                        field.set(new Udon_House());
                        target.addBlock(field.get(), x, y);
                    }
                    case SoilRoad.ID -> {
                        field.set(new SoilRoad());
                        target.addBlock(field.get(), x, y);
                    }
                    case SoilRoadCorner1.ID -> {
                        field.set(new SoilRoadCorner1());
                        target.addBlock(field.get(), x, y);
                    }
                    case BlueFlower.ID -> {
                        field.set(new BlueFlower());
                        target.addBlock(field.get(), x, y);
                    }
                }
            }
        }
    }
